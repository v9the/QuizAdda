    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (validateData() == true) {//PrintWriter writer = new PrintWriter (sock.getOutputStream());
            try {
                Socket socket = new Socket( QuizApp.IPAddress , QuizApp.PortNo);
                ObjectOutputStream oos = new ObjectOutputStream(socket.getOutputStream());

                User obj = new User();
                obj.UserID=jTextField1.getText();
                obj.UserName = jTextField2.getText();
                char[] arr = jPasswordField1.getPassword();
                obj.UserPassword = new String(arr);
                obj.CommandName="register";
                oos.writeObject(obj);
                
                ObjectInputStream ois = new ObjectInputStream(socket.getInputStream());
                boolean b1=false;
                try {
                    b1 = (boolean) ois.readObject();
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                if(b1)
                {
                    JOptionPane.showMessageDialog(null, "Registration Successful");
                    this.dispose();
                }

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

