    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Socket socket = new Socket(QuizApp.IPAddress, QuizApp.PortNo);
            ObjectOutputStream oos = new ObjectOutputStream(socket.getOutputStream());

            User obj = new User();
            obj.UserID = jTextField1.getText();
            char[] pwrd = jPasswordField1.getPassword();
            obj.UserPassword = new String(pwrd);
            obj.CommandName = "LogIn";

            oos.writeObject(obj);

            ObjectInputStream ois = new ObjectInputStream(socket.getInputStream());
            User us2 = (User) ois.readObject();

            ois.close();
            oos.close();
            socket.close();
            
            if (us2.UserNumber > 0) {

                QuizApp.LoggedUserNo = us2.UserNumber;
                QuizApp.LoggedUserPassword=us2.UserPassword;
                this.dispose();
                
                Welcome objWelcome =new Welcome(null, true);
                objWelcome.setVisible(true);
               
            } else {
                JOptionPane.showMessageDialog(this, "User Id or password incorrect!");
            }

        } catch (Exception ex) {
            Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


