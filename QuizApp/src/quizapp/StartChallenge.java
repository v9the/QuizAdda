    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //        i++;
        int OID = 0;

        if (jRadioButton1.isSelected()) {
            OID = q.Options.get(0).OptionID;
        } else if (jRadioButton2.isSelected()) {
            OID = q.Options.get(1).OptionID;
        } else if (jRadioButton3.isSelected()) {
            OID = q.Options.get(2).OptionID;
        } else if (jRadioButton4.isSelected()) {
            OID = q.Options.get(3).OptionID;
        }

        q.AttemptedOptionID = OID;

        if (counter < 4) {
            counter++;
            loadQuestion();

            buttonGroup1.clearSelection();

            if (counter == 4) {
                jButton1.setText("Submit");
            }
        } else {
            try {
                Socket socket = new Socket(QuizApp.IPAddress, QuizApp.PortNo);
                ObjectOutputStream oos = new ObjectOutputStream(socket.getOutputStream());

                challenge.UserNo = QuizApp.LoggedUserNo;

                challenge.CommandName = "#Attempt#";
                challenge.StatusNo=3;
                oos.writeObject(challenge);
                oos.close();
                socket.close();
                
           
                StartChallenge.this.dispose();
                //objR.setVisible(true);
                Wait obj=new Wait(null,true);
                obj.startWaiting(challenge.ChallengeID);

            } catch (IOException ex) {
                Logger.getLogger(StartChallenge.class.getName()).log(Level.SEVERE, null, ex);
            }

     
        }
    }//GEN-LAST:event_jButton1ActionPerformed

